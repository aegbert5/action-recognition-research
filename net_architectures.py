class CNN_1(nn.Module):
    def __init__(self):
        super(CNN_1, self).__init__()
        # 1 input image channel, 6 output channels, 5x5 square convolution
        # kernel
        # Reduced 9 channels to 7
        self.sigmoid = nn.Sigmoid()
        
        num_blacklist = Config.blacklist_channels.shape[0]
        self.conv1 = nn.Conv2d(int(RecognitionDataset.num_channels - num_blacklist), 16, (11, 6))
        self.conv2 = nn.Conv2d(16, 30, (11, 6))
        # an affine operation: y = Wx + b
        #self.fc1 = nn.Linear(30 * 10 * 80, 120) 
        #ORIGINAL was (10, 80), now (15, 80)
        self.fc1 = nn.Linear(30 * 15 * 80, 120)
        self.fc2 = nn.Linear(120, 84)
        self.fc3 = nn.Linear(84, RecognitionDataset.num_classifications)

    def forward(self, x):
        # Max pooling over a (2, 2) window
        #x = F.max_pool2d(F.relu(self.conv1(x)), (2, 2))
        # If the size is a square you can only specify a single number
        #x = F.max_pool2d(F.relu(self.conv2(x)), 2)
        x = F.relu(self.conv1(x))
        x = F.relu(self.conv2(x))
        x = x.view(-1, self.num_flat_features(x))
        x = F.relu(self.fc1(x))
        x = F.relu(self.fc2(x))
        x = self.sigmoid(self.fc3(x))
        return x

    def num_flat_features(self, x):
        size = x.size()[1:]  # all dimensions except the batch dimension
        num_features = 1
        for s in size:
            num_features *= s
        return num_features

